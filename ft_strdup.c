#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	length;
	size_t	i;
	char	*s2;

	length = ft_strlen(s) + 1;
	s2 = (char *)malloc(length * sizeof(char));
	if (!s2)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
